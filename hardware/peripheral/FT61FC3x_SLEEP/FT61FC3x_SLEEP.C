#include "SYSCFG.h"
//========================================================
#define  unchar     unsigned char 

#define  led1		PA1
#define  led2		PA0
 /*-------------------------------------------------
 *  函数名：POWER_INITIAL
 *	功能：  上电系统初始化
 *  输入：  无
 *  输出：  无
 --------------------------------------------------*/	
void POWER_INITIAL (void) 
{
	OSCCON = 0B01110001;	//IRCF=111=16MHz/4T=4MHz，0.25us
    OPTION = 0B00001000;    //Bit3=1 WDT，Bit[2:0]=000=WDT RATE 1:1
	INTCON = 0;  			//暂禁止所有中断
    
	PORTA  = 0B00000000;		
	TRISA  = 0B00000000;	//PA输入输出 0-输出 1-输入
							//PA0,PA1-OUT						
	PORTC  = 0B00000000; 	
	TRISC  = 0B00000000;	//PC输入输出 0-输出 1-输入  
								
	WPUA   = 0B00000000;    //PA端口上拉控制 1-开上拉 0-关上拉                         
	WPUC   = 0B00000000;    //PC端口上拉控制 1-开上拉 0-关上拉
}
 /*-------------------------------------------------
 *  函数名：DelayUs
 *  功能：  短延时函数 
 *  输入：  Time 延时时间长度 延时时长Time us
 * 	输出：  无 
 -------------------------------------------------*/
void DelayUs(unsigned char Time)
{
	unsigned char a;
	for(a=0;a<Time;a++)
	{
		NOP();
	}
}                  
/*------------------------------------------------- 
 * 	函数名：DelayMs
 * 	功能：  短延时函数--16M-4T--大概快1%左右.
 * 	输入：  Time 延时时间长度 延时时长Time ms
 * 	输出：  无 
 -------------------------------------------------*/
void DelayMs(unsigned char Time)
{
	unsigned char a,b;
	for(a=0;a<Time;a++)
	{
		for(b=0;b<5;b++)
		{
		 	DelayUs(98);      //快1%
		}
	}
}
/*------------------------------------------------- 
 * 	函数名：DelayS
 * 	功能：  短延时函数
 * 	输入：  Time 延时时间长度 延时时长Time S
 * 	返回：  无 
 -------------------------------------------------*/
void DelayS(unsigned char Time)
{
	unsigned char a,b;
	for(a=0;a<Time;a++)
	{
		for(b=0;b<10;b++)
		{
		 	DelayMs(100); 
		}
	}
}